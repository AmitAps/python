#Sets Don't Allow Duplicates

#Spotting sets in your code
#Sets start and end with a curly brace.
#Objects are separated from one another by a comma.

vowels = {'a', 'e', 'e', 'i', 'o', 'u', 'u'}

print(vowels)


#The fact that a set is enclosed in curly braces can often result in your brain mistaking a
#set for a dictionary, which is also enclosed in curly braces. The key difference is the use
#of the colon character (:) in dictionaries to separate keys from values. The colon never appears in a set, only commas.
